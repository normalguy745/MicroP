.INCLUDE "M32DEF.INC"

;RESET TABLE
.ORG 0x00
JMP INITIALIZE

;EXTERNAL INTERRUPT 1
.ORG 0x04

;TIMER1 OVERFLOW INTERUPT
.ORG 0x12

;INITIALIZATION
.ORG 0x100

INITIALIZE:
;STACK
LDI R16, HIGH (RAMEND)
OUT SPH, R16
LDI R16, LOW (RAMEND)
OUT SPL, R16

;IO
LDI R16, POR

MAIN:


DELAY200:
LDI R24, 200
LOOP:
DEC R24
BRNE LOOP
RET